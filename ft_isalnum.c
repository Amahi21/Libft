#include "libft.h"

int	ft_isalnum(int sym)
{
	if (sym >= '0' && sym <= '9')
		return (1);
	else if (sym >= 'a' && sym <= 'z')
		return (1);
	else if (sym >= 'A' && sym <= 'Z')
		return (1);
	else
		return (0);
}
